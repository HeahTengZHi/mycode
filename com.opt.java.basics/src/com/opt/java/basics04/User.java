package com.opt.java.basics04;

public class User {
	private String ID;
	private String Password;
	
	public void setID(String iD) {
		this.ID = iD;
	}
	
	public String getID() {
		return ID;
	}
	
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		this.Password = password;
	}
	
	
}
